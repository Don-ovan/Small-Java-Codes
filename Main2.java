package com.company;

public class Main {

    public static void main(String args[]) {
        int Test = 0b100;
        System.out.println("100 Binary: " + Test);
        Test = 0100;
        System.out.println("100 Octal: " + Test);
        Test = 0x100;
        System.out.println("100 Hexadecimal: " + Test);
    }
}
